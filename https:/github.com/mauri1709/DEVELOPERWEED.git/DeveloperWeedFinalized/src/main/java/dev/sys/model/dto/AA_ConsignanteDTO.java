package dev.sys.model.dto;

public class AA_ConsignanteDTO {
	private Integer id_consignante;
	private String nom_consignante;
	private String id_giro;
	private String direccion;
	private String id_comuna;
	private String telefono;
	private String contacto;
	public Integer getId_consignante() {
		return id_consignante;
	}
	public void setId_consignante(Integer id_consignante) {
		this.id_consignante = id_consignante;
	}
	public String getNom_consignante() {
		return nom_consignante;
	}
	public void setNom_consignante(String nom_consignante) {
		this.nom_consignante = nom_consignante;
	}
	public String getId_giro() {
		return id_giro;
	}
	public void setId_giro(String id_giro) {
		this.id_giro = id_giro;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getId_comuna() {
		return id_comuna;
	}
	public void setId_comuna(String id_comuna) {
		this.id_comuna = id_comuna;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getContacto() {
		return contacto;
	}
	public void setContacto(String contacto) {
		this.contacto = contacto;
	}
	
	

}
