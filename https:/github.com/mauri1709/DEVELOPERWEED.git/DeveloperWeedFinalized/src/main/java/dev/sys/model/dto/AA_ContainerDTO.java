package dev.sys.model.dto;

public class AA_ContainerDTO {
	private Integer n_container;
	private String tipo_container;
	private String medida_container;
	private String naviera;
	public Integer getN_container() {
		return n_container;
	}
	public void setN_container(Integer n_container) {
		this.n_container = n_container;
	}
	public String getTipo_container() {
		return tipo_container;
	}
	public void setTipo_container(String tipo_container) {
		this.tipo_container = tipo_container;
	}
	public String getMedida_container() {
		return medida_container;
	}
	public void setMedida_container(String medida_container) {
		this.medida_container = medida_container;
	}
	public String getNaviera() {
		return naviera;
	}
	public void setNaviera(String naviera) {
		this.naviera = naviera;
	}
	
	

}
